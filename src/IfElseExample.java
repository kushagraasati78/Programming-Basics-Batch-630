
public class IfElseExample {
    public static void main(String[] args) {

        int marks=27;
        
        if(marks>=60){
            System.out.println("Div-I");
        }else if(marks>=45){
            System.out.println("Div-II");
        }else if(marks>=33){
            System.out.println("Div-III");
        }else{
            System.out.println("Fail");
        }
        
        
    }
}
