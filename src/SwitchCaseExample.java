
public class SwitchCaseExample {
    public static void main(String[] args) {
        int day=2;  //byte, short, int, String
        
        switch(day){
            case 1:
                System.out.println("Monday");
            break ;
            case 2:
                System.out.println("Tuesday");
            break; 
        }
        
        
        
    }
}
